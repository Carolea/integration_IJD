    <h2>LA CARTE JEUNES EUROPÉENNE</h2>

    <span class="bold">Avec plus de 60.000 avantages disponibles dans 37 pays d’Europe*, la Carte Jeunes Européenne est le nouveau sésame pour voyager tout en profitant d’un maximum de bons plans.</span>

    <p>A travers des réductions dans des domaines aussi divers que les transports, le sport, la culture ou encore les loisirs, la carte permet à tous les jeunes, quelle que soit leur nationalité, de pouvoir découvrir de nouvelles activités, bouger, s’informer en France et à travers l’Europe… Un seul critère pour obtenir cette carte : avoir entre 12 et 30 ans. La carte est valable 1 an pour seulement 10 euros et fonctionne dans les 37 pays du programme.</p><br>


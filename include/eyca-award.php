<h2>LA CARTE JEUNES EUROPÉENNE RÉCOMPENSÉE AUX EYCA EXCELLENCE AWARDS ÉDITION 2017 !</h2>

<p>
Lire l’article en anglais <a href="http://www.eyca.org/news/256/here-are-the-winners-of-the-2017-eyca-excellence-awards">ici</a>.

<span class="bold"> Chaque année, la EYCA décerne des prix EYCA Excellence Award par catégorie à ses membres pour l’excellence des travaux fournis.</span>

Les gagnants ont donc été récompensés le 3 juin dernier lors de l’événement de clôture de la Conférence Annuelle de la EYCA organisée cette année à Belgrade en Serbie.

Voici la liste des 6 prix décernés ainsi que les gagnants :

<span class="bold">Prix du développement – HÖOK &amp; Educational Authority, Hongrie</span>
Décerné à la Hongrie pour ses fructueux efforts en matière de développement :  la Carte Jeune Européenne hongroise est au service de plus d’un million de jeunes dans son pays.

<span class="bold">Prix de la qualité des avantages – Agència Catalana de la Joventut, Espagne</span>
L’organisation catalane a remporté le prix des « meilleures » réductions proposées aux jeunes, aujourd’hui  elle donne la possibilité aux jeunes d’accéder à plus de 6 500 bons plan et projets dans sa région.

<span class="bold">Prix de la connaissance – Young Scot, Ecosse</span>
Comme chaque année, la Carte Jeune Européenne écossaise « Young Scot » développe son programme en mettant en avant de jeunes écossais porteurs du programme. Elle a d’ailleurs donné la possibilité à ses jeunes membres de s’exprimer et d’être récompensés en organisant leur propre cérémonie les « Young Scot Award ». L’Université St Andrew, établissement de renommée mondiale, a d’ailleurs offert la possibilité aux vainqueurs de venir étudier en son sein.

<span class="bold">Prix de la mobilité jeunesse et citoyenneté active – CJP, Pays-Bas</span>
Le CJP des Pays-bas a su mettre en place le Young Impact, un programme innovant qui encourage les jeunes aux Pays-Bas à être une force de changement pour améliorer l’environnement dans lequel ils vivent.

<span class="bold">Prix Communication et Relation publiques – Association IJD France, France</span>
La formidable campagne conçue et mise en œuvre par l’Association IJD France, a fortement contribué au lancement réussi de la Carte Jeunes Européenne en France, en septembre 2016. La EYCA a donc récompensé cette belle réalisation et a décidé d’attribuer à Association IJD le prix de la communication et des relations publiques.

<span class="bold">   Prix de l’engagement et de la contribution à la EYCA – Young Scot, Ecosse</span>
Le dernier Award a été décerné une nouvelle fois à Young Scot pour son partage d’expérience, son engagement et l’ensemble de ses contributions pertinentes tout au long de l’année avec les autres membres de la EYCA, l’organisation écossaise est un moteur important au développement de ce projet européen.

L’ensemble de l’équipe de la Carte Jeunes Européenne est très honorée de cette récompense et remercie l’ensemble des organisateurs de cette belle soirée.
</p>

